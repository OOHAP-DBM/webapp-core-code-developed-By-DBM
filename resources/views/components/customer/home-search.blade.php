        // Smooth scroll and tab highlight for navbar buttons
        document.addEventListener('DOMContentLoaded', function() {
            function setActiveTab(tab) {
                var hoardingsBtn = document.getElementById('tab-hoardings');
                var spotsBtn = document.getElementById('tab-spots');
                if (tab === 'hoardings') {
                    hoardingsBtn.classList.add('text-gray-900', 'border-b-2', 'border-gray-400');
                    hoardingsBtn.classList.remove('text-gray-500', 'border-transparent');
                    spotsBtn.classList.remove('text-gray-900', 'border-b-2', 'border-gray-400');
                    spotsBtn.classList.add('text-gray-500', 'border-transparent');
                } else {
                    spotsBtn.classList.add('text-gray-900', 'border-b-2', 'border-gray-400');
                    spotsBtn.classList.remove('text-gray-500', 'border-transparent');
                    hoardingsBtn.classList.remove('text-gray-900', 'border-b-2', 'border-gray-400');
                    hoardingsBtn.classList.add('text-gray-500', 'border-transparent');
                }
            }

            document.querySelectorAll('[data-scroll-target]').forEach(function(btn) {
                btn.addEventListener('click', function(e) {
                    var target = btn.getAttribute('data-scroll-target');
                    var el = document.querySelector(target);
                    if (el) {
                        e.preventDefault();
                        // Set active tab
                        if (btn.id === 'tab-hoardings') {
                            setActiveTab('hoardings');
                        } else {
                            setActiveTab('spots');
                        }
                        window.scrollTo({
                            top: el.getBoundingClientRect().top + window.scrollY - 100,
                            behavior: 'smooth'
                        });
                    }
                });
            });
            // Set initial active tab
            setActiveTab('hoardings');
        });
    </script>
